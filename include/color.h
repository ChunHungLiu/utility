#ifndef _COLOR_H
#define _COLOR_H

#define RED "\33[31m"
#define GREEN "\33[32m"
#define ORANGE "\33[33m"
#define BLUE "\33[34m"
#define PURPLE "\33[35m"
#define CYAN "\33[36m"
#define COLOREND "\33[0m"

// \33[0m;30m;40m == \33[0;30;40m
// 0 means normal, 1 means light, 31~36 means RED, GREEN, ORANGE, BLUE, PURPLE, CYAN, 40up means the background color

#endif // _COLOR_H
